//Example for Method Overloading

public class Main{

public static void main(String [] args){

   System.out.println("Hello");
    m1(1,2);
    m1("Multiverse");
}

public static void m1(int a,int b){

System.out.print(a);
System.out.println(b);

}

public static void m1(String str){

System.out.println(str);

}

}
